"""
ML-Assisted Benders Decomposition for p-Hub-Arc Problem

This implementation keeps the original Benders method exact.

ML is used only for:
1. Predicting promising hub arcs for warm start.
2. Ranking OD pairs during callback separation.
3. Optionally ranking generated cuts.

Exactness is preserved because:
- all Benders cuts are still generated by the exact dual separation algorithm;
- if ML-prioritized OD pairs do not generate cuts, the callback runs full separation.
"""

import math
import statistics
import time
from typing import Any, Dict, List, Optional, Tuple

import gurobipy as gp
from gurobipy import GRB

try:
    from sklearn.ensemble import RandomForestClassifier
    from sklearn.pipeline import Pipeline
    from sklearn.preprocessing import StandardScaler
    import joblib

    SKLEARN_AVAILABLE = True
except ImportError:
    SKLEARN_AVAILABLE = False

from algo1_hub_arc import separation_algorithm_dual
from algo3_hub_arc import (
    compute_allocation_cost,
    phase1_solve_mp,
)


# ============================================================
# 1. Preprocessing
# ============================================================

def preprocess(n: int, W: List[List[float]], D: List[List[float]]):
    """
    Build C, L, K, cost_map, arcs_sorted from W and D.
    """
    N = range(n)
    H = [(u, v) for u in N for v in N if u != v]

    C: Dict[Tuple[int, int], List[float]] = {}
    K: Dict[Tuple[int, int], int] = {}
    L: Dict[Tuple[int, int], Dict[int, List[Tuple[int, int]]]] = {}
    cost_map: Dict[Tuple[int, int], Dict[Tuple[int, int], float]] = {}
    arcs_sorted: Dict[Tuple[int, int], List[Tuple[int, int]]] = {}

    for i in N:
        for j in N:
            if i == j:
                continue

            costs_ij = []
            cm = {}

            for u in N:
                for v in N:
                    if u == v:
                        continue

                    cost_uv = W[i][j] * (D[i][u] + D[u][v] + D[v][j])
                    costs_ij.append(cost_uv)
                    cm[(u, v)] = cost_uv

            unique_costs = sorted(set(round(c, 8) for c in costs_ij))

            C[(i, j)] = unique_costs
            K[(i, j)] = len(unique_costs)
            cost_map[(i, j)] = cm
            arcs_sorted[(i, j)] = sorted(cm.keys(), key=lambda a: cm[a])

            L[(i, j)] = {}
            for k_idx, target_cost in enumerate(unique_costs, start=1):
                arcs_k = [
                    (u, v)
                    for (u, v), c_val in cm.items()
                    if abs(c_val - target_cost) < 1e-8
                ]
                L[(i, j)][k_idx] = arcs_k

    od_pairs = [(i, j) for (i, j) in K if i != j and K[(i, j)] > 1]

    return H, C, L, K, cost_map, arcs_sorted, od_pairs


# ============================================================
# 2. Basic utility functions
# ============================================================

def safe_mean(values):
    return sum(values) / len(values) if values else 0.0


def safe_std(values):
    if len(values) <= 1:
        return 0.0
    return statistics.pstdev(values)


def selected_arcs_from_y(y: Dict[Tuple[int, int], float], threshold: float = 0.5):
    return [a for a, val in y.items() if val > threshold]


def compute_total_solution_cost(
    n: int,
    y_sol: Dict[Tuple[int, int], float],
    C: Dict,
    K: Dict,
    cost_map: Dict,
    arcs_sorted: Dict,
):
    """Computes true allocation cost for a given y solution."""
    total = 0.0

    for (i, j), Kij in K.items():
        if i == j:
            continue

        if Kij == 1:
            total += C[(i, j)][0]
        else:
            total += compute_allocation_cost(
                i,
                j,
                y_sol,
                cost_map.get((i, j), {}),
                arcs_sorted.get((i, j), []),
            )

    return total


# ============================================================
# 3. Arc-level ML features
# ============================================================

def build_arc_feature(
    n: int,
    W: List[List[float]],
    D: List[List[float]],
    cost_map: Dict,
    arc: Tuple[int, int],
):
    u, v = arc

    costs = []
    for i in range(n):
        for j in range(n):
            if i == j:
                continue
            costs.append(cost_map[(i, j)][(u, v)])

    outgoing_flow_u = sum(W[u][j] for j in range(n) if j != u)
    incoming_flow_u = sum(W[i][u] for i in range(n) if i != u)
    outgoing_flow_v = sum(W[v][j] for j in range(n) if j != v)
    incoming_flow_v = sum(W[i][v] for i in range(n) if i != v)
    total_flow = sum(W[i][j] for i in range(n) for j in range(n) if i != j)

    return [
        float(u),
        float(v),
        float(D[u][v]),
        float(outgoing_flow_u),
        float(incoming_flow_u),
        float(outgoing_flow_v),
        float(incoming_flow_v),
        float(outgoing_flow_u + incoming_flow_u),
        float(outgoing_flow_v + incoming_flow_v),
        float(total_flow),
        float(min(costs)),
        float(max(costs)),
        float(safe_mean(costs)),
        float(safe_std(costs)),
    ]


def build_arc_dataset_from_solved_instances(training_instances: List[Dict[str, Any]]):
    X = []
    y = []

    for inst in training_instances:
        n = inst["n"]
        W = inst["W"]
        D = inst["D"]
        selected = set(tuple(a) for a in inst["selected_arcs"])

        H, _, _, _, cost_map, _, _ = preprocess(n, W, D)

        for arc in H:
            X.append(build_arc_feature(n, W, D, cost_map, arc))
            y.append(1 if arc in selected else 0)

    return X, y


def train_arc_selection_model(
    training_instances: List[Dict[str, Any]],
    model_path: Optional[str] = None,
):
    if not SKLEARN_AVAILABLE:
        raise ImportError(
            "scikit-learn is not installed. Install: pip install scikit-learn joblib"
        )

    X, y = build_arc_dataset_from_solved_instances(training_instances)

    if len(set(y)) < 2:
        raise ValueError(
            "Training labels contain only one class. "
            "Need both selected and non-selected arcs in training data."
        )

    model = Pipeline(
        steps=[
            ("scaler", StandardScaler()),
            (
                "rf",
                RandomForestClassifier(
                    n_estimators=300,
                    max_depth=None,
                    min_samples_leaf=2,
                    class_weight="balanced",
                    random_state=42,
                ),
            ),
        ]
    )

    model.fit(X, y)

    if model_path is not None:
        joblib.dump(model, model_path)

    return model


def load_ml_model(model_path: str):
    if not SKLEARN_AVAILABLE:
        raise ImportError("joblib / scikit-learn is not installed.")
    return joblib.load(model_path)


# ============================================================
# 4. ML warm start
# ============================================================

def predict_arc_scores(
    n: int,
    W: List[List[float]],
    D: List[List[float]],
    H: List[Tuple[int, int]],
    cost_map: Dict,
    arc_model=None,
):
    scores = {}

    if arc_model is not None:
        for arc in H:
            x = [build_arc_feature(n, W, D, cost_map, arc)]
            try:
                prob = arc_model.predict_proba(x)[0][1]
            except Exception:
                prob = float(arc_model.predict(x)[0])
            scores[arc] = float(prob)
    else:
        for arc in H:
            costs = [
                cost_map[(i, j)][arc]
                for i in range(n)
                for j in range(n)
                if i != j
            ]
            scores[arc] = -safe_mean(costs)

    return scores


def build_ml_warm_start(
    n: int,
    p: int,
    W: List[List[float]],
    D: List[List[float]],
    H: List[Tuple[int, int]],
    cost_map: Dict,
    arc_model=None,
):
    scores = predict_arc_scores(n, W, D, H, cost_map, arc_model)
    ranked_arcs = sorted(H, key=lambda a: scores[a], reverse=True)
    chosen = set(ranked_arcs[:p])
    y_ml = {a: 1.0 if a in chosen else 0.0 for a in H}
    return y_ml, scores


# ============================================================
# 5. OD-pair features for ML separation ranking
# ============================================================

def best_selected_cost_for_od(
    pair: Tuple[int, int],
    y_bar: Dict[Tuple[int, int], float],
    arcs_sorted: Dict,
    cost_map: Dict,
):
    i, j = pair
    for arc in arcs_sorted[(i, j)]:
        if y_bar.get(arc, 0.0) > 0.5:
            return cost_map[(i, j)][arc]
    return max(cost_map[(i, j)].values())


def build_od_feature(
    n: int,
    W: List[List[float]],
    D: List[List[float]],
    C: Dict,
    K: Dict,
    cost_map: Dict,
    arcs_sorted: Dict,
    pair: Tuple[int, int],
    y_bar: Dict[Tuple[int, int], float],
    theta_bar: Dict[Tuple[int, int], float],
):
    i, j = pair
    costs = C[(i, j)]
    theta_val = theta_bar.get((i, j), 0.0)
    best_cost = best_selected_cost_for_od(pair, y_bar, arcs_sorted, cost_map)
    violation_proxy = max(0.0, best_cost - theta_val)

    return [
        float(i),
        float(j),
        float(W[i][j]),
        float(D[i][j]),
        float(K[(i, j)]),
        float(min(costs)),
        float(max(costs)),
        float(safe_mean(costs)),
        float(safe_std(costs)),
        float(theta_val),
        float(best_cost),
        float(violation_proxy),
    ]


def rank_od_pairs(
    n: int,
    W: List[List[float]],
    D: List[List[float]],
    C: Dict,
    K: Dict,
    cost_map: Dict,
    arcs_sorted: Dict,
    od_pairs: List[Tuple[int, int]],
    y_bar: Dict[Tuple[int, int], float],
    theta_bar: Dict[Tuple[int, int], float],
    od_model=None,
):
    scored = []

    for pair in od_pairs:
        x = build_od_feature(
            n, W, D, C, K, cost_map, arcs_sorted, pair, y_bar, theta_bar
        )

        if od_model is not None:
            try:
                score = od_model.predict_proba([x])[0][1]
            except Exception:
                score = float(od_model.predict([x])[0])
        else:
            score = x[-1]

        scored.append((pair, float(score)))

    scored.sort(key=lambda item: item[1], reverse=True)
    return [pair for pair, _ in scored]


